module Abaqus
  KnownKeywords = {
    "*NODE"     => Node,
    "*ELEMENT"  => Element,
    "*NSET"     => Nset,
    "*ELSET"    => Elset,
    "*BOUNDARY" => Bc,
    "*MATERIAL" => Material,
    "*STEP"     => Step,
    "*MPC"      => MPC,
    "*SYSTEM"   => System,
  }
  Property_Keywords = [
    "*SHELL SECTION" ,
    "*BEAM SECTION" ,
    "*SOLID SECTION" ,
    "*BEAM GENERAL SECTION",
    "*SPRING",
    #"",
  ]
  Property_Keywords.each do |key|
    KnownKeywords[key] = Property
  end

  class SkipParser
    def self.parse(line, body)
      #line = body.gets or return nil
      return Inp.parse_data(body) {}
    end
  end
  module_function
  def parse(f,name="default_model")
    model = Model.new(name)
    Node.reset_converter  # clear system definition
    model.with_bind do
      line = f.gets or break
      while line[0,2] == "**"
        line = f.gets  or break
      end
      keyword, opts = Inp.parse_command(line)
      raise "first keyword must be *heading" unless keyword == "*HEADING"
      line = Inp.parse_data(f) {} # skip
      while line
        keyword, ops = Inp.parse_command(line)
        klass = KnownKeywords[keyword] || SkipParser
        line, args = klass.parse(line, f)
      end
    end
    return model
  end
end


